import mongoose from 'mongoose'

const auditLogSchema = new mongoose.Schema(
  {
    user: { type: String, required: true, trim: true, index: true },
    role: {
      type: String,
      enum: ['Admin', 'Super Admin', 'Teacher', 'Staff', 'Student'],
      required: true,
      index: true,
    },
    action: {
      type: String,
      enum: [
        'LOGIN',
        'LOGOUT',
        'STUDENT_CREATE',
        'STUDENT_UPDATE',
        'STUDENT_DELETE',
        'COURSE_CREATE',
        'COURSE_UPDATE',
        'COURSE_DELETE',
        'ATTENDANCE_MARK',
        'ATTENDANCE_UPDATE',
        'ATTENDANCE_DELETE',
        'GRADE_CREATE',
        'GRADE_UPDATE',
        'GRADE_DELETE',
        'SETTINGS_CHANGE',
        'REPORT_EXPORT',
        'DOCUMENT_UPLOAD',
        'DOCUMENT_DELETE',
        'NOTIFICATION_CREATE',
        'NOTIFICATION_READ',
        'NOTIFICATION_DELETE',
        'ADMIN_CREATE',
        'ADMIN_UPDATE',
        'ADMIN_STATUS_CHANGE',
        'ADMIN_ROLE_CHANGE',
        'ADMIN_PASSWORD_RESET',
        'ADMIN_DELETE',
        'DEPARTMENT_CREATE',
        'DEPARTMENT_UPDATE',
        'DEPARTMENT_DELETE',
        'PROGRAM_CREATE',
        'PROGRAM_UPDATE',
        'PROGRAM_DELETE',
        'ACADEMIC_YEAR_CREATE',
        'ACADEMIC_YEAR_UPDATE',
        'ACADEMIC_YEAR_DELETE',
        'SEMESTER_CREATE',
        'SEMESTER_UPDATE',
        'SEMESTER_DELETE',
        'STAFF_CREATE',
        'STAFF_UPDATE',
        'STAFF_DELETE',
        'STUDENT_COURSE_ENROLL',
        'STUDENT_COURSE_UPDATE',
        'STUDENT_COURSE_DELETE',
        'COURSE_ASSIGNMENT_CREATE',
        'COURSE_ASSIGNMENT_UPDATE',
        'COURSE_ASSIGNMENT_DELETE',
      ],
      required: true,
      index: true,
    },
    module: {
      type: String,
      enum: [
        'Auth',
        'Students',
        'Courses',
        'Attendance',
        'Grades',
        'Settings',
        'Reports',
        'Documents',
        'Notifications',
        'Admins',
        'Departments',
        'Programs',
        'AcademicYears',
        'Semesters',
        'Staff',
        'StudentCourses',
        'CourseAssignments',
      ],
      required: true,
      index: true,
    },
    description: { type: String, required: true, trim: true },
    ipAddress: { type: String, trim: true, default: '' },
    browser: { type: String, trim: true, default: 'Unknown' },
    device: { type: String, trim: true, default: 'Unknown' },
    metadata: { type: mongoose.Schema.Types.Mixed, default: {} },
    timestamp: { type: Date, default: Date.now, index: true },
  },
  { timestamps: true },
)

auditLogSchema.index({ user: 'text', description: 'text', action: 'text', module: 'text' })
auditLogSchema.index({ role: 1, module: 1, timestamp: -1 })

export const AuditLog = mongoose.model('AuditLog', auditLogSchema)
